int	ft_sqrt(int nb)
{
	int	i;
	int	sub;

	i = 0;
	sub = 1;
	while (nb > 0)
	{
		nb -= sub;
		sub += 2;
		i++;
	}
	if (nb == 0)
		return (i);
	return (0);
}
