int ft_is_prime(int nb)
{
	int	mod;

	if (nb == 2 || nb == 3 || nb == 5)
		return 1;
	if (nb > 6)
	{
		mod = nb % 10;
		if (mod == 1 || mod == 3 || mod == 7 || mod == 9)
		{
			mod = nb % 6;
			if(mod == 1 || mod == 5)
				return (1);
		}
	}
	return (0);
}
