void	ft_swap(int **tab)
{
	int	*tmp;

	tmp = tab[1];
	tab[1] = tab[0];
	tab[0] = tmp;
}

int	ft_min(int a, int b)
{
	if (a > b)
		return (b);
	return (a);
}
