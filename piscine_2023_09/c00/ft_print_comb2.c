#include <unistd.h>

void ft_print_comb2(void)
{
	char	a1;
	char	a2;
	char	b1;
	char	b2;

	a1 = '0';
	a2 = '0';
	while (a1 != '9' || a2 != '9')
	{
		if(a2 == '9')
		{
			b2 = '0';
			b1 = a1 + 1;
		} else {
			b2 = a2 + 1;
			b1 = a1;
		}
		while (b1 <= '9' && b2 <= '9')
		{
			write(1, &a1, 1);
			write(1, &a2, 1);
			write(1, " ", 1);
			write(1, &b1, 1);
			write(1, &b2, 1);
			if(!(a1 == '9' && a2 == '8'))
				write(1, ", ", 2);
			if(b2 == '9')
			{
				b2 = '0';
				b1++;
			} else {
				b2++;
			}	
		}
		if(a2 == '9')
		{
			a2 = '0';
			a1++;
		} else {
			a2++;
		}
	}
}

int main(void)
{
	ft_print_comb2();
	return 0;
}
