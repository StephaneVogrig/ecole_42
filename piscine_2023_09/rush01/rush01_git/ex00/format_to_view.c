int	*format_to_view(char **argv, int view[4][4])
{
	int	i;
	int	x;
	int	y;

	i = 0;
	x = 0;
	y = 0;
	while (x < 4)
	{
		y = 0;
		while(y < 4)
		{
			if(argv[1][i] > 48 && argv[1][i] < 57)
			{
				view[x][y] = argv[1][i] - 48;
				y++;
			}
			i++;
		}
		x++;	
	}
	return (&view[4][4]);
}
