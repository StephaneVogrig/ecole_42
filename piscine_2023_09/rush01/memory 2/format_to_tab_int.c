int	*format_to_tab_int(char **argv, int view[4][4])
{
	int	i;
	int	x;
	int	y;
	int	pair;

	i = 0;
	x = 0;
	y = 0;
	pair = 1;
	while (argv[1][i] != '\0')
	{	
		if (pair == 1)
		{
			y = i / 4;
			x = i % 4;
			view[y][x] = argv[1][i] - 48;
		}
		pair = pair * -1;
		i++;
	}
}
